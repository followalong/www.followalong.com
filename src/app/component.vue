import MultiEventStore from '../state/multi-event-store.js'
import runners from '../state/runners.js'
import Queries from '../queries/index.js'

export default {
  components: {
    FeedEntry,
    SideBar,
    TopBar
  },
  props: {
    state: {
      type: Object,
      default: () => new MultiEventStore('follow-along', 'v2.1', runners)
